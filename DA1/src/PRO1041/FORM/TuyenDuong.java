    private void btn_timActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timActionPerformed
        int i;
        String makh = txt_tim.getText();
        for (i = 0; i < list.size(); i++) {
            TuyenDuongDAO td = list.get(i);
            if (td.MaTuyen.equals(makh) == true) {
                curredIndex = i;
            tbl_td.setRowSelectionInterval(curredIndex, curredIndex);              
                DislayNV();
                break;
            }
        }
        if (i == list.size()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy tuyến");
        }
    }//GEN-LAST:event_btn_timActionPerformed

