    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
         int ret = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Có",
                JOptionPane.YES_NO_OPTION);
        if (ret != JOptionPane.YES_OPTION) {
            return;
        }
        Connection c = null;
        PreparedStatement ps = null;
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            c = DriverManager.getConnection(url, username, password);
            ps = c.prepareStatement("Delete From TuyenDuong where MaChuyen = ?");
            ps.setString(1, txt_ma.getText());
            String ten = txt_ten.getText();
            ret = ps.executeUpdate();
            if (ret != -1) {
                JOptionPane.showMessageDialog(this, "Đã xóa tuyến đường " + ten);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            try {
                if (c != null) {
                    c.close();
                }
                if (ps != null) {
                    ps.close();
                    loadDataToTable();
                    LoadData();
                }
            } catch (Exception ex2) {
                ex2.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, username, password);
            String sql = "update TuyenDuong set TenChuyen=?, DiemDi=?, DiemDen=? where MaChuyen=?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, txt_ten.getText());
            st.setString(2, txt_diemdi.getText());   
            st.setString(3, txt_diemden.getText());
            st.setString(4, txt_ma.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Sửa thông tin tuyến đường thành công");
            con.close();
            loadDataToTable();
            LoadData();

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btn_suaActionPerformed

