    public void loadDataToTable() {
        try {
            tblModel.setRowCount(0);
            Connection con = DriverManager.getConnection(url, username, password);
            Statement st = con.createStatement();
            String sql = "select  * from TuyenDuong";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Vector r = new Vector();
                r.add(rs.getInt(1));
                r.add(rs.getString(2));
                r.add(rs.getString(3));
                r.add(rs.getString(4));
                

                tblModel.addRow(r);
            }
            tbl_td.setModel(tblModel);
            con.close();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
    public void LoadData(){
         Connection conn = null;
         Statement stm = null;
        try {                      
            conn = DriverManager.getConnection(url, username, password);
            stm = conn.createStatement();
            String sql = "";
            sql = "select  * from TuyenDuong";
            ResultSet rs = stm.executeQuery(sql);
            list.clear();
            
            while(rs.next()){
            String MaTuyen = rs.getString("MaChuyen");         
            String TenTuyen = rs.getString("TenChuyen");
            String DiemDi = rs.getString("DiemDi");
            String DiemDen = rs.getString("DiemDen");          
            TuyenDuongDAO tdd= new TuyenDuongDAO(MaTuyen, TenTuyen, DiemDi, DiemDen);
            list.add(tdd);
          }
            curredIndex=0;
            DislayNV();
            
            rs.close();
            stm.close();
            conn.close();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        
    }
      
